package com.ecam;

public class Point {
    private int x,y;

    public void setX(int newValue) {
        this.x = newValue;
    }

    public void setY(int newValue) {
        this.y = newValue;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}